package clientAPI;

public class ResposeInfo {
    private String body;
    private String code;

    public void ResposeInfo(){
    }

    public String getBody() {
        return body;
    }

    public void setBody(String body) {
        this.body = body;
    }

    public String getCode() {
        return code;
    }

    public void setCode(String code) {
        this.code = code;
    }
}
